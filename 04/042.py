# Вхідні дані
lst = [0, 1, 7, 2, 4, 8]

# Перевіряємо, чи список не є порожнім
if lst:
    # Знаходимо суму елементів із парними індексами
    even_index_sum = sum(lst[i] for i in range(0, len(lst), 2))

    # Перемножуємо цю суму на останній елемент списку
    result = even_index_sum * lst[-1]
else:
    # Якщо список порожній, результат завжди 0
    result = 0

# Виводимо результат
print(result)
