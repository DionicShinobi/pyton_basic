# Вхідний список, (значення можно змінювати за бажанням)
lst = [9, 0, 7, 31, 0, 45, 0, 45, 0, 45, 0, 0, 96, 0]

# Індекс для ненульових елементів
position = 0

# Переміщаємо всі ненульові елементи на початок списку
for i in range(len(lst)):
    if lst[i] != 0:
        lst[position] = lst[i]
        position += 1

# Заповнюємо кінець списку нулями
for i in range(position, len(lst)):
    lst[i] = 0

# Результат
print(lst)
